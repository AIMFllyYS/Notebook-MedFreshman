#!/usr/bin/env python3
"""Generate rec-01 through rec-04 quiz JSON from recording transcripts."""
import json
from pathlib import Path

OUT = Path(__file__).resolve().parent.parent / "content" / "quiz" / "maogai"


def save(chapter_id, questions):
    sc = sum(1 for q in questions if q["type"] == "single_choice")
    mc = sum(1 for q in questions if q["type"] == "multiple_choice")
    diff = {"basic": 0, "medium": 0, "hard": 0}
    for q in questions:
        diff[q["difficulty"]] += 1
    data = {
        "subjectId": "maogai",
        "chapterId": chapter_id,
        "generatedAt": "2026-06-21",
        "examConfig": {
            "source": "docs/refer/考试题型分布.md",
            "totalPoints": 100,
            "timeLimit": 50,
        },
        "questions": questions,
        "summary": {
            "totalQuestions": len(questions),
            "byType": {"single_choice": sc, "multiple_choice": mc, "essay": 4},
            "bySource": {"current_chapter": len(questions)},
            "byDifficulty": diff,
        },
    }
    path = OUT / f"{chapter_id}.json"
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    pts = sum(q["points"] for q in questions)
    print(f"Wrote {path.name}: {pts} points, sc={sc} mc={mc} {diff}")


def Q(id_, qtype, diff, stem, answer, explanation, ref, chapter, **kw):
    o = {
        "id": id_,
        "type": qtype,
        "difficulty": diff,
        "source": "current_chapter",
        "sourceChapter": chapter,
        "points": kw.get("points", 2),
        "stem": stem,
        "answer": answer,
        "hint": kw.get("hint", ""),
        "explanation": explanation,
        "sourceRef": ref,
    }
    if kw.get("label"):
        o["label"] = kw["label"]
    if kw.get("options"):
        o["options"] = kw["options"]
    if qtype == "essay":
        o["total_points"] = kw.get("total_points", o["points"])
        o["scoring_criteria"] = kw["scoring_criteria"]
    return o


