"""Generate physics study-guide example files for ch05-ch07."""
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1] / "content" / "examples" / "physics"


def write_example(section: str, nn: int, slug: str, label: str, question: str, solution: str, answer: str):
    ch_num = int(section.split(".")[0])
    path = ROOT / f"ch{ch_num:02d}" / section / f"EX{nn:02d}_{slug}.md"
    path.parent.mkdir(parents=True, exist_ok=True)
    content = f""":::example{{label={label}}}
**题目**：{question}

**解**：
{solution}

**答案**：{answer}
:::
"""
    path.write_text(content, encoding="utf-8")
    return str(path)


# section counters
counters: dict[str, int] = {}


def add(section, slug, label, question, solution, answer):
    counters[section] = counters.get(section, 0) + 1
    return write_example(section, counters[section], slug, label, question, solution, answer)


